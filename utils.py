def get_input(prompt):
    return input(prompt)

def validate_name(name):
    # Перевірка імені: ім'я повинно містити хоча б одну букву
    return name.isalpha()

def validate_phone(phone):
    # Перевірка телефону: номер повинен містити лише цифри і мати довжину 10
    return phone.isdigit() and len(phone) == 10
